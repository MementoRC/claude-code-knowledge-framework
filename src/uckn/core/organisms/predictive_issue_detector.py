"""
UCKN Predictive Issue Detector Organism

Orchestrates rule-based and ML-driven issue detection.
Provides early warnings, confidence scores, and preventive measures.
Integrates with existing knowledge management and analytics systems.
"""

import logging
from datetime import datetime  # Added import for datetime
from typing import Any

from ..atoms.tech_stack_detector import TechStackDetector
from ..molecules.error_solution_manager import ErrorSolutionManager  # READ ONLY
from ..molecules.issue_detection_rules import IssueDetectionRules
from ..molecules.issue_prediction_models import IssuePredictionModels
from ..molecules.pattern_analytics import PatternAnalytics  # READ ONLY


class PredictiveIssueDetector:
    """
    A comprehensive system for detecting potential issues in codebases
    using a combination of rule-based analysis and machine learning models.
    """

    def __init__(
        self,
        tech_stack_detector: TechStackDetector,
        issue_detection_rules: IssueDetectionRules,
        issue_prediction_models: IssuePredictionModels,
        error_solution_manager: ErrorSolutionManager,
        pattern_analytics: PatternAnalytics,
    ):
        self.tech_stack_detector = tech_stack_detector
        self.issue_detection_rules = issue_detection_rules
        self.issue_prediction_models = issue_prediction_models
        self.error_solution_manager = error_solution_manager
        self.pattern_analytics = pattern_analytics
        self._logger = logging.getLogger(__name__)

    def detect_issues(
        self,
        project_path: str,
        code_snippet: str | None = None,
        context_description: str | None = None,
        project_id: str | None = None,
    ) -> list[dict[str, Any]]:
        """
        Detects potential issues in a project or specific code context.

        Args:
            project_path: The file system path to the project root.
            code_snippet: An optional code snippet to analyze (e.g., for real-time IDE integration).
            context_description: An optional natural language description of the context.
            project_id: Optional ID of the project in UCKN for context and feedback.

        Returns:
            A list of detected issues, each with type, description, severity, confidence,
            and suggested preventive measures.
        """
        self._logger.info(f"Starting issue detection for project: {project_path}")
        all_detected_issues = []

        # 1. Analyze project tech stack
        project_stack = self.tech_stack_detector.analyze_project(project_path)
        self._logger.debug(f"Detected tech stack: {project_stack}")

        # 2. Apply rule-based detection
        rule_based_issues = self.issue_detection_rules.analyze_project_for_rules(
            project_path
        )
        self._logger.info(
            f"Rule-based detection found {len(rule_based_issues)} issues."
        )
        all_detected_issues.extend(rule_based_issues)

        # 3. Prepare data for ML model
        ml_input_data = {
            "project_path": project_path,
            "tech_stack": project_stack,
            "code_snippet": code_snippet,
            "context_description": context_description,
            "project_id": project_id,
        }

        # 4. Apply ML model prediction
        if self.issue_prediction_models.is_available():
            ml_predicted_issues = self.issue_prediction_models.predict(ml_input_data)
            self._logger.info(f"ML model predicted {len(ml_predicted_issues)} issues.")
            all_detected_issues.extend(ml_predicted_issues)
        else:
            self._logger.warning(
                "ML prediction models not available or not trained. Skipping ML prediction."
            )

        # 5. Deduplicate and refine (simple example, more complex logic might be needed)
        # For now, just return all found issues. A real system might merge similar issues.

        self._logger.info(
            f"Issue detection complete. Total {len(all_detected_issues)} potential issues identified."
        )
        return all_detected_issues

