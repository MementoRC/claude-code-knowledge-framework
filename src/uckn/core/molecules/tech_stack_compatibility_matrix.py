import hashlib
import json
import logging
from datetime import datetime
from typing import Any, Optional

from ...storage import ChromaDBConnector


class TechStackCompatibilityMatrix:
    """
    UCKN Tech Stack Compatibility Matrix Molecule.

    Manages and represents compatibility relationships between different
    technology stacks using a graph-based approach stored in ChromaDB.
    It tracks compatibility scores, common patterns, and success rates
    for various technology combinations.

    Features:
    - Graph-based representation for complex compatibility relationships.
    - Defines relationships between technology components.
    - Assigns compatibility scores between different stacks (0.0-1.0).
    - Tracks common patterns for each technology combination (implicitly via metadata).
    - Records success rates and setup times (can be extended in metadata).
    - Updates dynamically based on pattern application results.
    """

    _COLLECTION_NAME = "tech_stack_compatibility"

    def __init__(self, chroma_connector: ChromaDBConnector):
        """
        Initializes the TechStackCompatibilityMatrix with a ChromaDB connector.

        Args:
            chroma_connector: An instance of ChromaDBConnector.
        """
        self.chroma_connector = chroma_connector
        self._logger = logging.getLogger(__name__)

        # IMPORTANT: For this molecule to function correctly, the ChromaDBConnector
        # must be updated to include the schema for "tech_stack_compatibility"
        # in its `_COLLECTION_SCHEMAS` dictionary. Without this, ChromaDBConnector's
        # internal metadata validation will fail for this collection, and
        # add/update operations will not succeed.
        if self._COLLECTION_NAME not in self.chroma_connector.collections:
            self._logger.error(
                f"ChromaDB collection '{self._COLLECTION_NAME}' is not initialized "
                "or recognized by the provided ChromaDBConnector. "
                "Please ensure ChromaDBConnector's `_COLLECTION_SCHEMAS` "
                f"includes the schema for '{self._COLLECTION_NAME}'."
            )

    def is_available(self) -> bool:
        """
        Checks if the ChromaDB connector is available.

        Returns:
            True if ChromaDB is connected and ready, False otherwise.
        """
        return self.chroma_connector.is_available()

    @staticmethod
    def _generate_combo_id(ts_a: list[str], ts_b: list[str]) -> str:
        """
        Generates a consistent ID for a technology stack combination.
        The ID is generated by sorting and hashing the combined list of technologies
        to ensure (A, B) and (B, A) yield the same ID.

        Args:
            ts_a: List of technologies in the first stack.
            ts_b: List of technologies in the second stack.

        Returns:
            A unique string ID for the combination.
        """
        # Sort individual lists for consistency
        sorted_ts_a = sorted(ts_a)
        sorted_ts_b = sorted(ts_b)

        # Combine and sort the two lists of technologies
        combined_sorted_techs = sorted(sorted_ts_a + sorted_ts_b)

        # Create a stable string representation
        combo_string = json.dumps(combined_sorted_techs)

        # Hash the string to create a unique ID
        return hashlib.sha256(combo_string.encode("utf-8")).hexdigest()

    def add_tech_stack_combo(
        self, ts_a: list[str], ts_b: list[str], score: float, description: str = ""
    ) -> str | None:
        """
        Adds a new technology stack compatibility combination to the matrix.

        Args:
            ts_a: The first technology stack (list of technologies).
            ts_b: The second technology stack (list of technologies).
            score: Compatibility score between 0.0 and 1.0.
            description: A description of the compatibility.

        Returns:
            The ID of the added combination if successful, None otherwise.
        """
        if not self.is_available():
            self._logger.error("ChromaDB not available, cannot add tech stack combo.")
            return None

        if not (0.0 <= score <= 1.0):
            self._logger.error(
                f"Invalid score {score}. Score must be between 0.0 and 1.0."
            )
            return None

        combo_id = self._generate_combo_id(ts_a, ts_b)
        now_iso = datetime.now().isoformat()

        metadata = {
            "tech_stack_a": sorted(
                ts_a
            ),  # Store sorted for consistent retrieval/comparison
            "tech_stack_b": sorted(
                ts_b
            ),  # Store sorted for consistent retrieval/comparison
            "score": score,
            "description": description,
            "created_at": now_iso,
            "updated_at": now_iso,
            "combo_id": combo_id,
        }

        # A dummy document is needed for ChromaDB, as it's primarily text-based.
        # The actual "knowledge" is in the metadata for this collection.
        document_text = f"Compatibility between {', '.join(ts_a)} and {', '.join(ts_b)}"

        # ChromaDBConnector's add_document requires an embedding.
        # Since this molecule doesn't have a SemanticSearch dependency,
        # a dummy embedding is provided.
        dummy_embedding = [
            0.0
        ] * 384  # Common embedding dimension for sentence transformers

        success = self.chroma_connector.add_document(
            collection_name=self._COLLECTION_NAME,
            doc_id=combo_id,
            document=document_text,
            embedding=dummy_embedding,
            metadata=metadata,
        )
        return combo_id if success else None

    def get_compatibility_score(
        self, ts_a: list[str], ts_b: list[str]
    ) -> float | None:
        """
        Retrieves the compatibility score between two technology stacks.

        Args:
            ts_a: The first technology stack.
            ts_b: The second technology stack.

        Returns:
            The compatibility score (float) if found, None otherwise.
        """
        if not self.is_available():
            self._logger.warning(
                "ChromaDB not available, cannot get compatibility score."
            )
            return None

        combo_id = self._generate_combo_id(ts_a, ts_b)
        result = self.chroma_connector.get_document(
            collection_name=self._COLLECTION_NAME, doc_id=combo_id
        )
        if result and "metadata" in result:
            return result["metadata"].get("score")
        return None

    def get_compatibility_details(
        self, ts_a: list[str], ts_b: list[str]
    ) -> dict[str, Any] | None:
        """
        Retrieves full details of a specific compatibility combination.

        Args:
            ts_a: The first technology stack.
            ts_b: The second technology stack.

        Returns:
            A dictionary containing the full compatibility details, or None if not found.
        """
        if not self.is_available():
            self._logger.warning(
                "ChromaDB not available, cannot get compatibility details."
            )
            return None

        combo_id = self._generate_combo_id(ts_a, ts_b)
        result = self.chroma_connector.get_document(
            collection_name=self._COLLECTION_NAME, doc_id=combo_id
        )
        if result and "metadata" in result:
            return result["metadata"]
        return None

    def update_compatibility_score(
        self,
        ts_a: list[str],
        ts_b: list[str],
        new_score: float | None = None,
        new_description: str | None = None,
    ) -> bool:
        """
        Updates an existing technology stack compatibility combination.

        Args:
            ts_a: The first technology stack.
            ts_b: The second technology stack.
            new_score: The new compatibility score (0.0-1.0), optional.
            new_description: The new description, optional.

        Returns:
            True if updated successfully, False otherwise.
        """
        if not self.is_available():
            self._logger.warning(
                "ChromaDB not available, cannot update compatibility score."
            )
            return False

        if new_score is not None and not (0.0 <= new_score <= 1.0):
            self._logger.error(
                f"Invalid new score {new_score}. Score must be between 0.0 and 1.0."
            )
            return False

        combo_id = self._generate_combo_id(ts_a, ts_b)

        # Retrieve existing metadata to merge updates
        existing_doc = self.chroma_connector.get_document(
            collection_name=self._COLLECTION_NAME, doc_id=combo_id
        )
        if not existing_doc:
            self._logger.warning(
                f"Compatibility combo '{combo_id}' not found for update."
            )
            return False

        updated_metadata = existing_doc["metadata"].copy()
        if new_score is not None:
            updated_metadata["score"] = new_score
        if new_description is not None:
            updated_metadata["description"] = new_description

        updated_metadata["updated_at"] = datetime.now().isoformat()

        # No document text or embedding update needed unless specified,
        # but ChromaDB update method requires them to be passed as None if not changing.
        return self.chroma_connector.update_document(
            collection_name=self._COLLECTION_NAME,
            doc_id=combo_id,
            document=None,  # No change to document text
            embedding=None,  # No change to embedding
            metadata=updated_metadata,
        )

    def delete_tech_stack_combo(self, ts_a: list[str], ts_b: list[str]) -> bool:
        """
        Deletes a technology stack compatibility combination from the matrix.

        Args:
            ts_a: The first technology stack.
            ts_b: The second technology stack.

        Returns:
            True if deleted successfully, False otherwise.
        """
        if not self.is_available():
            self._logger.warning(
                "ChromaDB not available, cannot delete tech stack combo."
            )
            return False

        combo_id = self._generate_combo_id(ts_a, ts_b)
        return self.chroma_connector.delete_document(
            collection_name=self._COLLECTION_NAME, doc_id=combo_id
        )

    def get_all_compatibility_scores(self) -> list[dict[str, Any]]:
        """
        Retrieves all stored technology stack compatibility combinations.

        Returns:
            A list of dictionaries, each representing a compatibility combination.
        """
        if not self.is_available():
            self._logger.warning(
                "ChromaDB not available, cannot retrieve all compatibility scores."
            )
            return []

        all_docs = self.chroma_connector.get_all_documents(
            collection_name=self._COLLECTION_NAME
        )

        results = []
        for doc in all_docs:
            if "metadata" in doc:
                results.append(doc["metadata"])
        return results

    def search_compatibility(
        self, query_tech_stack: list[str], limit: int = 10, min_score: float = 0.0
    ) -> list[dict[str, Any]]:
        """
        Searches for compatibility scores related to a given technology stack.
        This method will search for combinations where `query_tech_stack` is either
        `tech_stack_a` or `tech_stack_b`.

        Args:
            query_tech_stack: The technology stack to search for.
            limit: Maximum number of results to return.
            min_score: Minimum compatibility score to include in results.

        Returns:
            A list of relevant compatibility records.
        """
        if not self.is_available():
            self._logger.warning("ChromaDB not available, cannot search compatibility.")
            return []

        # IMPORTANT: Due to the current ChromaDBConnector's API (which is read-only),
        # which primarily supports semantic search with embeddings, and lacks a direct
        # method for complex metadata-only queries (e.g., OR conditions on list types),
        # this method retrieves all documents and filters them in Python memory.
        # This approach may be inefficient for very large datasets.
        self._logger.warning(
            "Performing in-memory filtering for tech stack compatibility search "
            "due to ChromaDBConnector's current API limitations for metadata-only queries. "
            "This may be inefficient for large datasets."
        )

        sorted_query_tech_stack = sorted(query_tech_stack)
        all_combos = self.get_all_compatibility_scores()

        filtered_results = []
        for combo in all_combos:
            ts_a_stored = combo.get("tech_stack_a", [])
            ts_b_stored = combo.get("tech_stack_b", [])
            score = combo.get("score", 0.0)

            # Ensure stored lists are also sorted for consistent comparison
            if (
                sorted(ts_a_stored) == sorted_query_tech_stack
                or sorted(ts_b_stored) == sorted_query_tech_stack
            ):
                if score >= min_score:
                    filtered_results.append(combo)

            if len(filtered_results) >= limit:
                break

        return filtered_results
